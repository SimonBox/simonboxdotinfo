export function get_publications_data(){
    var bibtexParse = require('bibtex-parse-js');
    var pubs_json = bibtexParse.toJSON(bibtex_str);
    return pubs_json;
}

export function get_publication_by_key(key){
    const pubs_json = get_publications_data();
    for(var i = 0; i < pubs_json.length; i++)
    {
        if(pubs_json[i].citationKey === key)
        {
            return pubs_json[i];
        }
    }
}

